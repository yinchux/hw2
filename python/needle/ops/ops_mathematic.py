class Summation(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.sum(a, axis=self.axes)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        in_shape = node.inputs[0].shape
        if not self.axes:
            return broadcast_to(out_grad, in_shape)
        axes = self.axes if isinstance(self.axes, tuple) else (self.axes,)
        out_shape = list(in_shape)
        for axis in axes:
            out_shape[axis] = 1
        return broadcast_to(reshape(out_grad, tuple(out_shape)), in_shape)
        ### END YOUR SOLUTION


def summation(a, axes=None):
    return Summation(axes)(a)


class MatMul(TensorOp):
    def compute(self, a, b):
        ### BEGIN YOUR SOLUTION
        return a @ b
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        lhs, rhs = node.inputs
        grad_a = out_grad @ transpose(rhs)
        grad_b = transpose(lhs) @ out_grad
        # handle broadcasting
        if grad_a.shape != lhs.shape:
            axes = []
            for i in range(len(grad_a.shape) - len(lhs.shape)):
                axes.append(i)
            for i in range(len(lhs.shape)):
                if lhs.shape[i] == 1 and grad_a.shape[i + len(grad_a.shape) - len(lhs.shape)] != 1:
                    axes.append(i + len(grad_a.shape) - len(lhs.shape))
            if axes:
                grad_a = summation(grad_a, tuple(axes))
            grad_a = reshape(grad_a, lhs.shape)
        if grad_b.shape != rhs.shape:
            axes = []
            for i in range(len(grad_b.shape) - len(rhs.shape)):
                axes.append(i)
            for i in range(len(rhs.shape)):
                if rhs.shape[i] == 1 and grad_b.shape[i + len(grad_b.shape) - len(rhs.shape)] != 1:
                    axes.append(i + len(grad_b.shape) - len(rhs.shape))
            if axes:
                grad_b = summation(grad_b, tuple(axes))
            grad_b = reshape(grad_b, rhs.shape)
        return grad_a, grad_b
        ### END YOUR SOLUTION


def matmul(a, b):
    return MatMul()(a, b)


